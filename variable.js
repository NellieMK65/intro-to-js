// Variable - a container that can hold values for later retrieval

// Declaring
// 1. const - immutable (value cannot change)
const school = 'Moringa School';

console.log(school);

// school = 'KU';
// 2. let - mutable (value can change)
let age = 45;

console.log(age);

console.log(age);

// reassigning a new value
age = 34;

console.log(age);

let fullName = 'John Doe';

console.log(fullName);

fullName = 'Jane Doe';

console.log(fullName);
